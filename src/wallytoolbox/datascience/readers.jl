##############################################################################
# CSV
##############################################################################

function read_csv(fname;
        sep::Char = ',',
        comments = true,
        comment_char = '#'
    )::DataFrame
	data, header = readdlm(fname, sep; header=true, comments, comment_char)
	return DataFrame(data, vec(Symbol.(header)))
end

